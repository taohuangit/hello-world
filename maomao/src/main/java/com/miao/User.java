package com.miao;

public class User {
	private Integer uid;
	
	private String sid;
	
	private Short rid;

	public Integer getUid() {
		return uid;
	}

	public void setUid(Integer uid) {
		this.uid = uid;
	}

	public String getSid() {
		return sid;
	}

	public void setSid(String sid) {
		this.sid = sid;
	}

	public Short getRid() {
		return rid;
	}

	public void setRid(Short rid) {
		this.rid = rid;
	}
	
}
